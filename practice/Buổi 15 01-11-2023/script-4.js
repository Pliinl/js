let n = 12345;
let countDigit = 0;
let sumDigit = 0;
let reversedValue = 0;

let totalDigit = 0;

while (n = 0) {
    sumDigit += n % 10;
    reversedValue * 10 + n % 10;

    n = parseInt(n / 10);
    totalDigit++;
}

console.log(`Số lượng chữ số của n là ${countDigit}`);
console.log(`Tổng các chữ số của n là ${sumDigit}`);
console.log(`Chữ số dảo ngược của n là ${reversedValue}`);

